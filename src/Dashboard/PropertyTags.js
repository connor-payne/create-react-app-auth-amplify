export default function Dashboard() {
  const classes = useStyles();
  const [open, setOpen] = React.useState(true);

  const [tags, setTags] = React.useState([])
  const [propertyData, setPropertyData] = React.useState({})
  const { propertyId } = useParams();

    React.useEffect(() => {
        if (tags.length == 0) {
            setAllTags();
        } 
        if (!propertyData.id) {
          setProperty();
        }

       
    }, [tags, propertyData]);

  
  const setProperty = async () => {
      const property = await getPropertyById(propertyId);
      setPropertyData(property);
  }

  const setAllTags = async () => {
      const allTags = await getAllTags(propertyId);
      setTags(allTags);
  }

  const handleDrawerOpen = () => {
    setOpen(true);
  };

  const handleDrawerClose = () => {
    setOpen(false);
  };
  const fixedHeightPaper = clsx(classes.paper, classes.fixedHeight);

  return (
    <div className={classes.root}>
      <CssBaseline />
      <AppBar position="absolute" className={clsx(classes.appBar, open && classes.appBarShift)}>
        <Toolbar className={classes.toolbar}>
          <IconButton
            edge="start"
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            className={clsx(classes.menuButton, open && classes.menuButtonHidden)}
          >
            <MenuIcon />
          </IconButton>
          <Typography component="h1" variant="h6" color="inherit" noWrap className={classes.title}>
            Property Tags
          </Typography>
          <IconButton color="inherit">
            <Badge badgeContent={4} color="secondary">
              <NotificationsIcon />
            </Badge>
          </IconButton>
        </Toolbar>
      </AppBar>
      <Drawer
        variant="permanent"
        classes={{
          paper: clsx(classes.drawerPaper, !open && classes.drawerPaperClose),
        }}
        open={open}
      >
        <div className={classes.toolbarIcon}>
        <img src='/OALogo.png' alt='Onsite Analytics Logo' style={{ height: '4rem', float:'left'}}></img>
          <IconButton onClick={handleDrawerClose} >
            <ChevronLeftIcon />
          </IconButton>
        </div>
        <Divider />
        <List>{mainListItems}</List>
        {/* <Divider />
        <List>{secondaryListItems}</List> */}
      </Drawer>
      <main className={classes.content}>
        <div className={classes.appBarSpacer} />
        <Container maxWidth="lg" className={classes.container}>
            <Grid container spacing={3}>
                <Grid item xs={12}>
                    <Paper className={classes.paper}>
                        <PropertyTags tags={tags} propertyId={propertyId} refresh={setAllTags} propertyData={propertyData}/>
                    </Paper>
                </Grid>
            </Grid>
          <Box pt={4}>
            <Copyright />
          </Box>
        </Container>
      </main>
    </div>
  );
}